def translate_tool(text, lang="id"):
    if not text:
        return text
    if lang == "en":
        # First check old TOOL_TRANSLATIONS, then ID_TO_EN
        if text in TOOL_TRANSLATIONS:
            return TOOL_TRANSLATIONS[text]
        return TOOL_TRANSLATIONS_ID_TO_EN.get(text, text)
    elif lang == "id":
        return TOOL_TRANSLATIONS_EN_TO_ID.get(text, text)
    return text
